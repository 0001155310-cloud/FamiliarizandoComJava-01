package FamiliarizandoComaJava_Atv1.Atv1_30questoes;

public class DeclarandoVariaveis {

    public static void main(String[] args) {

        // Declarando as variáveis ============================
        int idade = 17; //Declara a variável "idade" com valor: 17
        double dinheiro = 1890.00; //Declara a variável "dinheiro" com valor: 1890.00
        char letra = 'J'; //Declara a variável "letra" com valor: 'J'
        boolean maiorDeIdade = false; //Declara a variável "maiorDeIdade" com valor: false

        //Exibindo as variáveis
        System.out.println("\n===== Exibindo Variáveis ======");

        System.out.println("Carlos tem " +idade + " anos de idade!");
        System.out.println("Pedro recebe R$" + dinheiro + " por mês em seu trabalho (coitado)");
        System.out.println("A primeira letra do nome de João é: " + letra);
        System.out.println("O Herinque é maior de idade?: " +maiorDeIdade);
        System.out.println("\n== Programa Encerrado! ==");




    }
}
